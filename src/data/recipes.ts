export function findMatchingRecipes(selectedIngredients: string[]): { recipe: Recipe; matchPercentage: number; matchedIngredients: string[]; missingIngredients: string[] }[] {
  if (selectedIngredients.length === 0) return [];
  
  const normalizeIngredient = (ing: string) => ing.toLowerCase().trim();
  
  const results = recipes.map(recipe => {
    const matchedIngredients = recipe.ingredients.filter(ing => 
      selectedIngredients.some(selected => {
        const normalizedIng = normalizeIngredient(ing);
        const normalizedSelected = normalizeIngredient(selected);
        return normalizedIng.includes(normalizedSelected) || 
               normalizedSelected.includes(normalizedIng) ||
               normalizedIng === normalizedSelected;
      })
    );
    
    const missingIngredients = recipe.ingredients.filter(ing => 
      !selectedIngredients.some(selected => {
        const normalizedIng = normalizeIngredient(ing);
        const normalizedSelected = normalizeIngredient(selected);
        return normalizedIng.includes(normalizedSelected) || 
               normalizedSelected.includes(normalizedIng) ||
               normalizedIng === normalizedSelected;
      })
    );
    
    const matchPercentage = Math.round((matchedIngredients.length / recipe.ingredients.length) * 100);
    
    return {
      recipe,
      matchPercentage,
      matchedIngredients,
      missingIngredients
    };
  });
  
  return results
    .filter(r => r.matchPercentage >= 20)
    .sort((a, b) => b.matchPercentage - a.matchPercentage);
}
